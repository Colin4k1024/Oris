//! Agent Code Fix Reuse Example
//!
//! This example demonstrates the Evolution system's ability to capture and reuse
//! agent code fix experience across similar bugs.
//!
//! Flow:
//! 1. Round 1: Agent encounters E0501 error (missing import), fixes it successfully
//! 2. The fix is captured as a Gene with signals: ["E0501", "import", "use std"]
//! 3. Round 2: Similar E0501 error occurs
//! 4. System extracts signals and tries to replay via existing Gene
//! 5. Result: Reuse success (capsule applied) or fallback to LLM
//!
//! Run with:
//! ```bash
//! cargo run -p evo_oris_repo --bin agent_code_fix_reuse --features "full-evolution-experimental,ollama"
//! ```
//!
//! Environment:
//! - OPENAI_API_KEY: Required for full LLM integration (fallback mode works without it)

use evo_oris_repo::{
    build_demo_evo, current_env_fingerprint, current_git_head, proposal_for, ExampleResult,
};
use oris_runtime::agent_contract::{AgentTask, ProposalTarget};
use oris_runtime::evolution::{EvoKernel, EvoSelectorInput as SelectorInput};

/// Helper to create a diff for adding an import statement to a new Rust file
/// Following the same format as proposal_diff in lib.rs
fn import_fix_diff(path: &str, import_stmt: &str, item_name: &str) -> String {
    format!(
        "diff --git a/{path} b/{path}\n\
         new file mode 100644\n\
         index 0000000..1111111\n\
         --- /dev/null\n\
         +++ b/{path}\n\
         @@ -0,0 +1,4 @@\n\
         +// Auto-generated by Oris Evolution\n\
         +{import_stmt}\n\
         +\n\
         +pub fn use_{item_name}() {{}}\n"
    )
}

#[tokio::main]
async fn main() -> ExampleResult<()> {
    println!("=== Agent Code Fix Reuse Example ===\n");

    let workspace_root = std::env::current_dir()?;
    let base_revision = current_git_head(&workspace_root);

    // Initialize Evolution Kernel with low promotion threshold for demo
    let evo = build_demo_evo("agent-code-fix-reuse", 1)?;

    // ============================================================
    // ROUND 1: First bug fix - capture as Gene
    // ============================================================
    println!("[Round 1] Agent encounters E0501: missing import");
    println!("  Code: let map = HashMap::new();  // E0501: cannot find type `HashMap`\n");

    let task_1 = AgentTask {
        id: "fix-missing-hashmap-import".into(),
        description: "Fix E0501 by adding HashMap import".into(),
    };
    let target_path = "docs/evo-hashmap-fix-generated.rs";
    let target_1 = ProposalTarget::Paths(vec![target_path.to_string()]);
    let proposal_1 = proposal_for(
        &task_1,
        &target_1,
        "code-agent",
        "add use std::collections::HashMap to fix E0501",
    );

    // Create a proper diff for a new file in docs/
    let fix_diff_1 = import_fix_diff(target_path, "use std::collections::HashMap;", "hashmap");

    println!("  Capturing mutation as Gene...");
    let capture_1 = evo
        .capture_from_proposal(
            &"round-1-fix".into(),
            &proposal_1,
            fix_diff_1,
            base_revision.clone(),
        )
        .await?;

    println!("  ✓ Gene created: {}", capture_1.gene.id);
    println!("  ✓ Capsule created: {}", capture_1.capsule.id);
    println!("  Signals: {:?}", capture_1.gene.signals);
    println!("  Strategy: {:?}", capture_1.gene.strategy);
    println!("  Confidence: {:.2}", capture_1.capsule.confidence);
    println!("  State: {:?}\n", capture_1.gene.state);

    // Store signals for replay test
    let captured_signals = capture_1.gene.signals.clone();
    let _captured_env = capture_1.capsule.env.clone();

    // ============================================================
    // ROUND 2: Similar bug - try to replay
    // ============================================================
    println!("[Round 2] Agent encounters similar E0501 error");
    println!("  Code: let mutex = Mutex::new(0);  // E0501: cannot find type `Mutex`\n");

    // Extract signals from the new error (simulating signal extraction)
    let new_signals = vec![
        "E0501".to_string(),
        "Mutex".to_string(),
        "cannot find type".to_string(),
        "import".to_string(),
    ];

    println!("  Extracted signals: {:?}", new_signals);
    println!("  Attempting replay via Gene selector...\n");

    let selector_input = SelectorInput {
        signals: new_signals,
        env: current_env_fingerprint(),
        spec_id: None,
        limit: 5,
    };

    let decision = evo
        .replay_or_fallback_for_run(&"round-2-replay".into(), selector_input)
        .await?;

    // ============================================================
    // Print Results
    // ============================================================
    println!("=== Replay Decision ===");
    println!("  Used capsule: {}", decision.used_capsule);
    println!("  Fallback to planner: {}", decision.fallback_to_planner);
    println!("  Reason: {}", decision.reason);

    if let Some(capsule_id) = &decision.capsule_id {
        println!("  Capsule ID: {}", capsule_id);
    }

    // Get detailed feedback
    let replay_feedback = EvoKernel::<evo_oris_repo::ExampleState>::replay_feedback_for_agent(
        &captured_signals,
        &decision,
    );

    println!("\n=== Replay Feedback ===");
    println!(
        "  Planner directive: {:?}",
        replay_feedback.planner_directive
    );
    println!("  Used capsule: {}", replay_feedback.used_capsule);
    println!(
        "  Reasoning steps avoided: {}",
        replay_feedback.reasoning_steps_avoided
    );
    println!("  Summary: {}", replay_feedback.summary);

    // ============================================================
    // Summary
    // ============================================================
    println!("\n=== Summary ===");
    if decision.used_capsule {
        println!("✓ SUCCESS: Gene was reused!");
        println!("  - No LLM call needed for similar fix");
        println!("  - Confidence: {:.2}", capture_1.capsule.confidence);
        println!("  - Next time similar error occurs, system will try reuse first");
    } else {
        println!("✗ FALLBACK: System would call LLM");
        println!("  - Reason: {}", decision.reason);
        println!("  - This is expected if signals don't match well enough");
        println!("  - After successful LLM fix, a new Gene will be created");
    }

    println!("\n=== Evolution Experience Reuse Demo Complete ===");
    Ok(())
}
